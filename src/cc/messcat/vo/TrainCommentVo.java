package cc.messcat.vo;

public class TrainCommentVo {
	
	private String comment;
	private String star;
	private String hour;
	private String vehicle;
	private String teach;
	private String attitude;
	private String appShareType;
	private String appShareUrl;
	
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getStar() {
		return star;
	}
	public void setStar(String star) {
		this.star = star;
	}
	public String getHour() {
		return hour;
	}
	public void setHour(String hour) {
		this.hour = hour;
	}
	public String getVehicle() {
		return vehicle;
	}
	public void setVehicle(String vehicle) {
		this.vehicle = vehicle;
	}
	public String getTeach() {
		return teach;
	}
	public void setTeach(String teach) {
		this.teach = teach;
	}
	public String getAttitude() {
		return attitude;
	}
	public void setAttitude(String attitude) {
		this.attitude = attitude;
	}
	public String getAppShareType() {
		return appShareType;
	}
	public void setAppShareType(String appShareType) {
		this.appShareType = appShareType;
	}
	public String getAppShareUrl() {
		return appShareUrl;
	}
	public void setAppShareUrl(String appShareUrl) {
		this.appShareUrl = appShareUrl;
	}

}
