package cc.messcat.vo;

public class VideoWallVo {

	private String headPhoto;//用戶的頭像
	private String nickName;//用戶暱稱
	private String content;//墻的文字內容
	private String wallImg;//墙上的图片
	public String getHeadPhoto() {
		return headPhoto;
	}
	public void setHeadPhoto(String headPhoto) {
		this.headPhoto = headPhoto;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWallImg() {
		return wallImg;
	}
	public void setWallImg(String wallImg) {
		this.wallImg = wallImg;
	}
	
}
